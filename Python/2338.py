alfabeto = {
    '=.===': "a",  # .-
    '===.=.=.=': 'b',  # -...
    '===.=.===.=': 'c',  # -.-.
    '===.=.=': 'd',  # -..
    '=': 'e',  # .
    '=.=.===.=': 'f',  # ..-.
    '===.===.=': 'g',  # --.
    '=.=.=.=': 'h',  # ....
    '=.=': 'i',  # ..
    '=.===.===.===': 'j',  # .---
    '===.=.===': 'k',  # -.-
    '=.===.=.=': 'l',  # .-..
    '===.===': 'm',  # --
    '===.=': 'n',  # -.
    '===.===.===': 'o',  # ---
    '=.===.===.=': 'p',  # .--.
    '===.===.=.===': 'q',  # --.-
    '=.===.=': 'r',  # .-.
    '=.=.=': 's',  # ...
    '===': 't',  # -
    '=.=.===': 'u',  # ..-
    '=.=.=.===': 'v',  # ...-
    '=.===.===': 'w',  # .--
    '===.=.=.===': 'x',  # -..-
    '===.=.===.===': 'y',  # -.--
    '===.===.=.=': 'z',  # --..
    'SPACE': " "
}

for i in range(int(input())):
    s = input().replace(".......", " SPACE ").split("...")
    s = " ".join(s)
    s = s.split()
    n = ''
    for i in s:
        n += alfabeto[i]
    print(n)
        
